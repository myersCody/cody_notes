def create_order_hierarchy(data, order_key="usage", group_key="node", order_dicts=[collections.OrderedDict(), collections.OrderedDict()]):
    """
        Creates an order map.
    """
    if isinstance(data, list):
        for value in data:
            order_dicts = create_order_hierarchy(value, order_key, group_key, order_dicts)
    elif isinstance(data, dict):
        if order_key in data.keys() and group_key in data.keys():
            order_dict = order_dicts[0]
            group_identifer = data.get(group_key)
            order_value = data.get(order_key)
            # prev_key = None
            if isinstance(order_value, dict) and "value" in order_value.keys():
                order_value = order_value.get("value")
                order_dict[group_identifer] = order_value
            return order_dicts
        if "currencys" in data.keys() and group_key in data.keys():
            order_dict = order_dicts[1]
            group_identifer = data.get(group_key)
            order_dict[group_identifer] = data
        for _, dikt_value in data.items():
            order_dicts = create_order_hierarchy(dikt_value, order_key, group_key, order_dicts)
    return order_dicts



